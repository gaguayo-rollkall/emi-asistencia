function getRandomTime(start, end) {
  // Split start and end times into hours and minutes
  const [startHour, startMinute] = start.split(':').map(Number);
  const [endHour, endMinute] = end.split(':').map(Number);

  // Calculate total minutes between start and end times
  const totalStartMinutes = startHour * 60 + startMinute;
  const totalEndMinutes = endHour * 60 + endMinute;
  const totalMinutesInRange = totalEndMinutes - totalStartMinutes;

  // Generate a random number of minutes within the range
  const randomMinutes = Math.floor(Math.random() * (totalMinutesInRange + 1));

  // Add random minutes to the start time
  const randomTotalMinutes = totalStartMinutes + randomMinutes;

  // Calculate the hour and minute components of the random time
  const randomHour = Math.floor(randomTotalMinutes / 60);
  const randomMinute = randomTotalMinutes % 60;

  // Format hour and minute components into a time string
  const formattedHour = String(randomHour).padStart(2, '0');
  const formattedMinute = String(randomMinute).padStart(2, '0');

  // Concatenate hour and minute components to create the random time string
  const randomTimeString = `${formattedHour}:${formattedMinute}`;

  return randomTimeString;
}

function getRandomArbitrary(min, max) {
  const minCeiled = Math.ceil(min);
  const maxFloored = Math.floor(max);
  return Math.floor(Math.random() * (maxFloored - minCeiled) + minCeiled); // The maximum is exclusive and the minimum is inclusive
}

